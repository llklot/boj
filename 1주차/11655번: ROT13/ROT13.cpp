#include<bits/stdc++.h>
using namespace std;
string s;
int main(){
    getline(cin, s);
    for(int i = 0; i < s.size(); i++){
        if('A' <= s[i] && s[i] <= 'Z'){
            if(s[i] + 13 > 'Z') s[i] -= 13;
            else s[i] += 13;
        }else if('a' <= s[i] && s[i] <= 'z'){
            if(s[i] > 'z') s[i] -= 26;
            else s[i] += 13;
        }
        cout << s[i];
    }
    return 0;
}